public class Cor {
    private Number r;
    private Number g;
    private Number b;

    public Cor(Number r, Number g, Number b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public void setR(Number r) {
        this.r = r;
    }

    public Number getR() {
        return r;
    }

    public void setG(Number g) {
        this.g = g;
    }

    public Number getG() {
        return g;
    }

    public void setB(Number b) {
        this.b = b;
    }

    public Number getB() {
        return b;
    }
}
